#!/usr/bin/env python3
"""
3-to_str.py: A simple module that defines the `to_str` function.

This module provides a type-annotated function `to_str` that
takes a float `n`
as an argument and returns its string representation.
"""


def to_str(n: float) -> str:
    """
    Returns the string representation of the input float.

    Args:
        n (float): Input float.

    Returns:
        str: String representation of `n`.
    """
    return str(n)
